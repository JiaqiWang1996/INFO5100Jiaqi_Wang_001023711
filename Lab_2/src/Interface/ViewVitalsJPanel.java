    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
 // TODO add your handling code here:
  int selectdrow=tbVitalSigns.getSelectedRow();
  if(selectdrow >=0){
      VitalSigns vs=(VitalSigns)tbVitalSigns.getValueAt(selectdrow, 0);
      txtBloodTemperature.setText(String.valueOf(vs.getBloodPressure()));
      txtTemperature.setText(String.valueOf(vs.getTemperature()));
      txtPulse.setText(String.valueOf(vs.getPulse()));
      txtDate.setText(String.valueOf(vs.getDate()));
  }
  else
      JOptionPane.showMessageDialog(null, "please select any row");
    }//GEN-LAST:event_btnViewDetailsActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectdrow=tbVitalSigns.getSelectedRow();
        if(selectdrow >=0){
             VitalSigns vs=(VitalSigns)tbVitalSigns.getValueAt(selectdrow, 0);
               vsh.deleteVitals(vs);
               JOptionPane.showMessageDialog(null, "Vital Sign has been deleted");
               populateTable();
            }
  else
      JOptionPane.showMessageDialog(null, "please select any row");
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed


