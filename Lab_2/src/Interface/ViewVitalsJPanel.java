public void populateTable(){
    DefaultTableModel dtm=(DefaultTableModel)tbVitalSigns.getModel();//within the method,theTable get refreshed
    dtm.setRowCount(0);//no rows in the Table
    
    for(VitalSigns vs:vsh.getVitalSignHistory()){
    Object row[]=new Object[2];
    row[0]=vs;
    row[1]=vs.getBloodPressure();
    dtm.addRow(row);
    }
}
